package com.ernestina.cursojava.aula13;

public class OperadoresAritmeticos {

	public static void main(String[] args) {
		
		int resultado = 1 + 9;
		
		System.out.println(resultado); // 10
		
		//resultado++;
		System.out.println(resultado++); //10 imprimiu o valor antes de incrementar.
		System.out.println(resultado); //11

	}

}
